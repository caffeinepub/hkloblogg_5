interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
}

export default function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [open, setOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState(CATEGORIES[0].id);

  const currentEmojis =
    CATEGORIES.find((c) => c.id === activeCategory)?.emojis ?? [];

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <button
          type="button"
          data-ocid="emoji.open_modal_button"
          title="Välj emoji"
          className="inline-flex items-center justify-center w-7 h-7 rounded text-muted-foreground hover:text-foreground hover:bg-muted transition-colors"
        >
          <SmilePlus className="w-4 h-4" />
        </button>
      </PopoverTrigger>
      <PopoverContent
        data-ocid="emoji.popover"
        className="w-72 p-0 overflow-hidden"
        align="start"
        side="top"
      >
        {/* Category tabs */}
        <div className="flex overflow-x-auto border-b border-border bg-muted/40 sticky top-0">
          {CATEGORIES.map((cat) => (
            <button
              key={cat.id}
              type="button"
              onClick={() => setActiveCategory(cat.id)}
              title={cat.label}
              className={`flex-shrink-0 px-2 py-1.5 text-sm transition-colors hover:bg-muted ${
                activeCategory === cat.id
                  ? "border-b-2 border-primary text-primary bg-background"
                  : "text-muted-foreground"
              }`}
            >
              {cat.label.split(" ")[0]}
            </button>
          ))}
        </div>
        {/* Emoji grid */}
        <div className="h-48 overflow-y-auto p-2">
          <div className="grid grid-cols-10 gap-0.5">
            {currentEmojis.map((emoji) => (
              <button
                key={`${activeCategory}-${emoji}`}
                type="button"
                onClick={() => {
                  onSelect(emoji);
                  setOpen(false);
                }}
                className="flex items-center justify-center w-6 h-6 text-sm rounded hover:bg-muted transition-colors"
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  );
}
